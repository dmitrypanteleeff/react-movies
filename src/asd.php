            <p style="text-align: justify;">Экоотель "ВКС-Кантри" уже 17 лет радует своих гостей, дарит им незабываемые впечатления, возможность насладиться лесными прогулками, рыбалкой, плаванием и&nbsp;другими видами&nbsp;активного или спокойного отдыха на природе.</p>

            <p style="text-align: justify;">Экоотель расположился в поселке Сосновый Бор Владимирской области на обширной живописной территории в 17 га.&nbsp;</p>

            <p style="text-align: justify;">Издревле эта территория называлась «кольцо Радогостья»&nbsp;и находилась в окружении четырех рек: Клязьмы, Киржача, Вольги и Шередарь. Наши предки выбрали это место&nbsp;как одно из самых красивых&nbsp;во всей Северо-Восточной Руси для поклонения своему главному богу Роду. От имени которого образовались следующие слова в русском языке: «природа», «род», «народ», «Родина», «урожай».</p>

            <p style="text-align: justify;">На огромной территории нетронутой природы располагалось большое количество&nbsp; дворянских усадеб, которые практически не сохранились до нашего времени.</p>

            <p style="text-align: justify;">В конце XVIII века часть земель Соснового Бора принадлежала роду князей Трубецких. А в начале XX века, перед революцией, земля была в собственности фабриканта Сергея Ивановича Зимина, человека с широким кругозором, мецената и театрала.</p>

