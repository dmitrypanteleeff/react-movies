            <p style="text-align: justify;">Со времен С. И. Зимина на территории произошли изменения: перестроены административный дом, женский (он же Барский дом), мужской корпус и птичник. Большая часть усадьбы сохранила свой облик.</p>

            <p style="text-align: justify;">Административный дом (позже «Спальный корпус №2», в наст. время «Изба») – место постоянного пребывания Зимина. В нем же располагались основные покои С.И. Зимина и его кабинет. Позже был переоборудован под корпус для отдыхающих и перестроен.</p>

            <p style="text-align: justify;">Барский дом (женский корпус, позже «Спальный корпус №1», в наст. время Корпус №6) – основные покои С.И. Зимина, позднее переоборудован под женский корпус и вмещал в себя 75 гостей. Был перестроен в нынешний &nbsp;Корпус №6, в котором во времена СССР («Сосновый Бор») располагались прачка, душевые для сотрудников, кафе, буфет и прокат.</p>

