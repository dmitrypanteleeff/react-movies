            <p style="text-align: justify;">Имение Зиминых располагалось на берегах реки Киржач, местах с истинно русской красотой природы. Гостями Сергея Ивановича в свое время были А. Чехов, А. Блок, К. Бальмонт. Здесь раздавался знаменитый голос лучшего друга и товарища Зимина Ф. И. Шаляпина. Говорят, когда он пел, «дребезжали стекла в окнах». Эта усадьба была любимым местом отдыха Шаляпина.</p>

            <p style="text-align: justify;">Сам Зимин любил устраивать веселые праздничные застолья и чаепития с почетными гостями. Во время мероприятий за столом обязательно звучали любимые песни в исполнении Шаляпина, стихи, обсуждение последних новостей, планов, играла задорная музыка.</p>

            <p style="text-align: justify;">После одного из таких посещений Исаак Ильич Левитан, любуясь местным ландшафтом, написал свою картину «Стога сена в лунную ночь». Эта картина сейчас висит над камином в Доме-музее А. П. Чехова в Ялте.</p>

            <p style="text-align: justify;">На территории усадьбы находился «Конный завод» Зимина. Сергей Иванович выбрал одно из самых красивых мест имения для строительства конюшни. В каменном здании длиной 120 метров выращивались рысаки, кавалерийские кони, тяжеловозы. Когда поголовье становилось взрослым, хозяин продавал его на пополнение армии, для спортивных состязаний, помещичьим хозяйствам.</p>

            <p style="text-align: justify;">Но наступила революция, и в 1917 году земли С. И. Зимина перешли к государству, а сам он остался консультантом при своем театре. Скончался меценат в Москве в 1942 году.</p>

            <p style="text-align: justify;">После гражданской войны (1917-1922 гг.) на месте имения пытались открыть колонию для несовершеннолетних преступников, но из-за сложностей с организацией охраны ее пришлось перенести на остров в поселок Введенский.</p>

            <p style="text-align: justify;">В 1934 году в этом красивейшем месте открылся Дом отдыха. Старую конюшню переоборудовали под спальный корпус. Было принято решение организовать здравницу для реабилитации раненых, которая затем была переименована в Дом отдыха №12 «Усад». Такое название она носила до Великой Отечественной войны, когда на базе отдыха был развернут госпиталь №1877, просуществовавший до 1945 г. Здесь сотни солдат и офицеров с помощью медсестер и врачей восстановились от ран и снова вернулись в строй.</p>

            <p style="text-align: justify;">В 1946 году в Доме отдыха №12 «Усад» был основан санаторий общего типа, с 1948 по 1959 годы – Дом отдыха «Усад» (развитием и перестройкой руководила администрация поселка Усад).</p>

