            <p style="text-align: justify;">В 1959 году Дом отдыха был перепрофилирован в санаторий для лечения больных заболеваниями желудочно-кишечного тракта. В 1967 году был построен бювет минеральных вод, тогда же здесь начали применять для лечения минеральную воду «Моршанка» курорта Моршин из Львовской области. В 1979 году после многочисленных изыскательских работ были получены источники минеральной воды для наружного применения, открыта собственная вода «Покровская».</p>

            <p style="text-align: justify;">Санаторий совершенствуется, расширяется и благоустраивается, внедряются новые методы и формы лечения, он занимает достойное место среди здравниц СССР. Размещался он на историческом месте усадьбы Зимина. А с 1985 года начали возводиться многоэтажные корпуса «Соснового Бора».</p>

            <p style="text-align: justify;">В 2003-2005 гг. территория была разделена: появился санаторий «Сосновый Бор» и Дом отдыха «ВКС-Кантри». «Сосновый Бор» функционировал до 2017 года, после чего нехватка бюджета привела к его закрытию.</p>

