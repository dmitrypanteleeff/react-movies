            <p style="text-align: justify;">Птичник (после «Танцевально-игровой зал», «Зал лечебной физкультуры», в наст. время Центральная площадка) – использовался для разведения домашней птицы, после был переоборудован.</p>

            <p style="text-align: justify;">Мужской корпус в 1935-1980 гг (также «спальный корпус №9», в наст. время «горка») – гостевой дом для мужчин, позже использовался как мужской корпус с палатами. Был снесен из-за аварийного состояния сооружения.</p>

            <p style="text-align: justify;">Конюшня (позже «Лечебный корпус», «Госпиталь», в наст. время Корпус №4) – во времена С.И. Зимина на этом месте располагалась конюшня, которая представляла собой деревянное строение с примыкающей к нему территорией, огороженной забором из частокола. На территории находился колодец, а также маленькая кузница. Со временем колодец был засыпан, кузница разрушена, а само здание во времена Великой Отечественной войны служило военным госпиталем. Во времена «Дома отдыха «Усад» в ней располагались лечебные кабинеты, лаборатории и процедурные. Сейчас &nbsp;это Корпус №4&nbsp;с гостевыми номерами, баром и диско-залом.</p>

            <p style="text-align: justify;">При С. И. Зимине на месте нынешней спортивной площадки был ипподром, где конюхи тренировали и пасли лошадей.</p>

            <p style="text-align: justify;">Мощеная дорога. Сохранилась по сегодняшний день со времен С.И. Зимина, несколько раз подвергалась уничтожению. Во времена «Соснового Бора» начался процесс разбора этой дороги в целях украшения фасада над фундаментом здания (сохранилось по сей день) нынешнего Корпуса №1, однако этот процесс в скором времени остановили, благодаря чему удалось сохранить исторический вид дороги.</p>

            <p style="text-align: justify;">За нынешним Корпусом №6&nbsp;со времен С. И. Зимина растет&nbsp;лиственница-долгожитель, рядом с ней сохранилась аллея старых лип.</p>

            <p style="text-align: justify;">Почта и библиотека 1935 г (в наст. время Reception). Здание служило «отделением связи» для гостей и сотрудников, а также местом отдыха и досуга. Позднее оно было перестроено.</p>

            <p style="text-align: justify;">В период существования санатория на месте нынешнего «Офиса» располагалась Регистратура. Именно там сразу после приезда отдыхающих расселяли по корпусам. Прием пищи проходил в снесенном ныне здании, располагавшемся между нынешними 3 и 4 корпусами. Там же находился бювет минеральных вод.</p>

            <p style="text-align: justify;">На территории «ВКС-Кантри» сохранились статуи «Материнское счастье», «Шалун» и «Девочка на коньках», созданные в 1935 году. В 30-е годы напротив нынешней «Избы» на месте детской площадки&nbsp;стоял памятник Сталину, приветственно махавшего рукой прибывшим гостям. Вокруг были разбиты клумбы, поставлены скамьи. Позднее памятник был убран, а в 1970-е годы к столетию В. И. Ленина на этом же месте установлен его бюст.</p>

            <p style="text-align: justify;">Сейчас "ВКС-Кантри" - одно из самых живописных мест отдыха в Подмосковье, место единения с самим собой. Благодаря красоте рек&nbsp;Киржач и Шередарь, чистейшему воздуху смешанного леса и аромату соснового бора здесь можно почувствовать себя отрешенным от шумного мегаполиса. А атмосферу домашнего уюта и доброжелательности для гостей старается создать заботливый персонал экоотеля .</p>
